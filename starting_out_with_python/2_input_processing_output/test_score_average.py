# Program to calculate the average of 3 test scores.

# Get the 3 scores from the user.'
test1 = float(input('Enter the first test score: '))
test2 = float(input('Enter the second test score: '))
test3 = float(input('Enter the third test score: '))

# Calculate the average of the 3 tests.
average = (test1 + test2 + test3) / 3.0

# Display the average.
print('The average score is', average)